# Liczby zmiennoprzecinkowe - floats (float) - reprezentują liczby rzeczywiste które mogą mieć część dziesiętną.
# Przykłady:
# -5.02, -3.14, -0.001, 0.001, 3.14

liczba_zmiennoprzecinkowa_typ_int = int(3.14)
liczba_zmiennoprzecinkowa_typ_float = float(3.14)

print(
    f'Podana liczba zmiennoprzecinkowa (ale zamieniona w całkowitą) to: {liczba_zmiennoprzecinkowa_typ_int}')
print(
    f'Podana liczba zmiennoprzecinkowa to: {liczba_zmiennoprzecinkowa_typ_float}')
