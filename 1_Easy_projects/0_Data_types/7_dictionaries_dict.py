# Słowniki - dictionaries (dict) - Reprezentują mapowanie pomiędzy kluczami i wartościami.
# Klucze muszą być unikalne i są używane do dostępu do odpowiadających im wartości.
# Przykład:
#     słownik = {'klucz1':'wartość1', 'klucz2':'wartość2'},

słownik = {'klucz1': 'wartość1', 'klucz2': 'wartość2'}
print(f'Podany słownik to: {słownik}')
